package kr.or.ddit.study01;

public class ShortCut {
	public static void main(String[] args) {
		/*
		 * 단축키
		 * ctrl + shift + l           : 모든단축키안내
		 * ctrl + space               : 자동완성
		 * ctrl + alt + 위아래 방향키 : 코드복사
		 * alt + 위아래 방향키        : 코드위치변경
		 * ctrl + shift + /           : 범위주석
		 * ctrl + shift + C           : 한줄주석/취소
		 * ctrl + n					  : 신규생성
		 * ctrl + d					  : 한줄삭제
		 * 
		 * ctrl + z                   : 실행취소
		 * ctrl + y                   : 실행취소의 취소
		 * ctrl + s                   : 저장
		 * ctrl + shift + s           : 전체저장
		 * 
		 * ctrl + f11                 : 컴파일, 소스실행
		 * ctrl + shift + f           : 정렬
		 * 
		 */
		
		System.out.println("1");
		System.out.println("2");
		System.out.println("3");		
	}
}
